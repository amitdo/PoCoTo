    public NewProjectVisualPanel3(NewProjectWizardPanel3 panel) {
        initComponents();
        changeSupport.addChangeListener(panel);
        
        try {
            String[] configurations = MainController.findInstance()
                    .getProfilerWebServiceStub()
                    .getConfigurations()
                    .getGetConfigurationsResponse()
                    .getConfigurations();
            jComboBox1.setModel(new DefaultComboBoxModel(configurations));
            jComboBox1.setSelectedItem(null);
            jComboBox1.addItemListener(this);

        } catch (RemoteException ex) {
        }
    }
    
    private final ChangeSupport changeSupport = new ChangeSupport(this);

    private void fireChange() {
        changeSupport.fireChange();
    }


    public String getConfiguration() {
        if (jComboBox1.getSelectedItem() == null) {
            return "";
        } else {
            return jComboBox1.getSelectedItem().toString();
        }
    }
    
    @Override
    public String getName() {
        return NbBundle.getBundle(NewProjectWizardPanel3.class).getString("Panel3.Name");
    }

    @Override
    public void itemStateChanged(ItemEvent ie) {
        fireChange();
    }
